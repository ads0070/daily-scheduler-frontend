    public String getRegister() {
        String status = "";
        
        if(!password.getText().equals(confirm_password.getText())) {
            confirm_password.setText("");
            confirm_password.requestFocus();
            return "pm";
        } else {
            User user = User.builder()
            .username(user_name.getText())
            .password(password.getText())
            .firstName(first_name.getText())
            .lastName(last_name.getText())
            .phoneNumber(phone_number.getText())
            .eMail(e_mail.getText())
            .build();
        
            UserService service = new UserService(user);
            
            try {
                status = service.sendPost("http://localhost:8080/users");
            } catch (Exception ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return status;
    }
    
    public String checkValue() {
        if (first_name.getText().equals("Enter Your First Name") || first_name.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Your First Name");
            first_name.setText("");
            first_name.requestFocus();
            return "fail";
        }
        if (last_name.getText().equals("Enter Your Last Name") || last_name.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Your Last Name");
            last_name.setText("");
            last_name.requestFocus();
            return "fail";
        }
        if (user_name.getText().equals("Enter your Username") || user_name.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter your Username");
            user_name.setText("");
            user_name.requestFocus();
            return "fail";
        }
        if (phone_number.getText().equals("Enter Your Phone Number") || phone_number.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Your Phone Number");
            phone_number.setText("");
            phone_number.requestFocus();
            return "fail";
        }
        if (password.getText().equals("jPasswordField1") || password.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Your Password");
            password.setText("");
            password.requestFocus();
            return "fail";
        }
        if (e_mail.getText().equals("Enter Your E-mail") || e_mail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Your E-mail");
            e_mail.setText("");
            e_mail.requestFocus();
            return "fail";
        }
        return "ok";
    }
    
