    private void joinIn1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_joinIn1MouseClicked
        // TODO add your handling code here:
        String status = checkValue();
        
        if (status.equals("ok")) {
            status = getRegister();
            if (status.contains("ok")) {
                ProfileService service = new ProfileService(file);
                try {
                    service.sendMultiPart(user_name.getText());
                } catch (Exception ex) {
                    Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                LogIn lg = new LogIn();
                lg.setVisible(true);
                lg.pack();
                lg.setLocationRelativeTo(null);
                lg.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                this.dispose();
            } else if (status.equals("pm")) {
                JOptionPane.showMessageDialog(null, "Password Mismatch");
            } else {
                JOptionPane.showMessageDialog(null, "Join Failed");
            }
        }
    }//GEN-LAST:event_joinIn1MouseClicked
    
